# Básicos de Python (incorporados directamente en el lenguaje)

# csv: El módulo csv implementa clases para leer y escribir datos tabulares en formato CSV. Permite a los programadores decir, «escribe estos datos en el formato preferido por Excel», 
# o «lee datos de este archivo que fue generado por Excel», sin conocer los detalles precisos del formato CSV usado por Excel. Los programadores también pueden describir los formatos 
# CSV entendidos por otras aplicaciones o definir sus propios formatos CSV para fines particulares.

# datetime: El módulo datetime proporciona clases para manipular fechas y horas.

# fileinput: Este módulo implementa una clase auxiliar y funciones para escribir rápidamente un bucle sobre una entrada estándar o una lista de archivos. 

# json: JSON (JavaScript Object Notation), specified by RFC 7159 (which obsoletes RFC 4627) and by ECMA-404, is a lightweight data interchange format inspired by JavaScript object literal 
# syntax (although it is not a strict subset of JavaScript 1 ).
# json expone una API familiar a los usuarios de los módulos de la biblioteca estándar marshal y pickle.

# os: Este módulo provee una manera versátil de usar funcionalidades dependientes del sistema operativo.

# math: Este módulo proporciona acceso a las funciones matemáticas definidas en el estándar de C.

# random: Este módulo implementa generadores de números pseudoaleatorios para varias distribuciones.

# subprocess: El módulo subprocess permite lanzar nuevos procesos, conectarse a sus pipes de entrada/salida/error y obtener sus códigos de resultado. Este módulo está destinado 
# a reemplazar múltiples módulos y funciones previamente existentes: os.system, os.spawn*

# tkinter: El paquete tkinter («interfaz Tk») es la interfaz por defecto de Python para el kit de herramientas de GUI Tk. Desarrollo de aplicaciones de escritorio


